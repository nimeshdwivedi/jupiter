    private boolean checkEmptyFields() {
        int i = 0;
        Border LineBorder = new LineBorder(Color.RED);
        if (jTextField3.getText().isEmpty() || jTextField3.getText() == null) {
            jTextField3.setBorder(LineBorder);
            i = 1;
        }
        if (!jTextField3.getText().isEmpty()) {
            jTextField3.setBorder(defaultBorder);
        }

        if (jTextField2.getText().isEmpty() || jTextField2.getText() == null) {
            jTextField2.setBorder(LineBorder);
            i = 2;
        }
        if (!jTextField2.getText().isEmpty()) {
            jTextField2.setBorder(defaultBorder);
        }

        if (String.valueOf(jPasswordField1.getPassword()).isEmpty() || String.valueOf(jPasswordField1.getPassword()) == null) {
            jPasswordField1.setBorder(LineBorder);
            i = 3;
        }
        if (!String.valueOf(jPasswordField1.getPassword()).isEmpty()) {
            jPasswordField1.setBorder(defaultBorder);
        }

        if (jTextField5.getText().isEmpty() || jTextField5.getText() == null) {
            jTextField5.setBorder(LineBorder);
            i = 4;
        }
        if (!jTextField5.getText().isEmpty()) {
            jTextField5.setBorder(defaultBorder);
        }

        if (jTextField6.getText().isEmpty() || jTextField6.getText() == null) {
            jTextField6.setBorder(LineBorder);
            i = 5;
        }
        if (!jTextField6.getText().isEmpty()) {
            jTextField6.setBorder(defaultBorder);
        }

        if (jRadioButton1.isSelected() && (jTextField8.getText().isEmpty() || jTextField8.getText() == null)) {
            jTextField8.setBorder(LineBorder);
            i = 6;
        }
        if (jRadioButton1.isSelected() && !jTextField8.getText().isEmpty()) {
            jTextField8.setBorder(defaultBorder);
        }

        if (jRadioButton2.isSelected() && (jTextField7.getText().isEmpty() || jTextField7.getText() == null)) {
            jTextField7.setBorder(LineBorder);
            i = 7;
        }
        if (jRadioButton2.isSelected() && !jTextField7.getText().isEmpty()) {
            jTextField7.setBorder(defaultBorder);
        }
        return i > 0;
    }

