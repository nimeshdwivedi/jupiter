    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jLabel7.setForeground(Color.BLACK);
        jLabel7.setText("");
        List<String> selectedValuesList = jList1.getSelectedValuesList();
        List<String> errorConn = new ArrayList<>(selectedValuesList);
        int initialSize = errorConn.size();
        if (selectedValuesList.size() > 0) {
            for (String s : selectedValuesList) {
                try {
                    Reference connRef = new XMLHelper.ConnectionHelper().getDBReference(s);
                    if (connRef != null) {
                        if (new XMLHelper.ConnectionHelper().deleteDBConnection(s)) {
                            somethingChanged = true;
                            System.out.println("Deleted " + s);
                            errorConn.remove(s);
                        }
                    } else {
                        System.out.println("Connection Reference found null, cannot delete " + s);
                    }
                } catch (JAXBException ex) {
                    Logger.getLogger(ManageConnection.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        if (errorConn.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Successfully deleted connections: " + errorConn.toString().replace("[", "").replace("]", ""));
        } else {
            JOptionPane.showMessageDialog(null, "Cannot delete connections: " + errorConn.toString().replace("[", "").replace("]", ""));
        }
        dispose();
        new ManageConnection().setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

